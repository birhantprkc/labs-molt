def _collect_prompt_batch(dataloader_iter, num_prompts: int):
    """Draw up to `num_prompts` items from the prompt dataloader.

    Returns an exhaustion flag indicating whether the iterator is drained *after*
    collecting the returned prompts. Callers should still process any partial
    batch that was collected before exhaustion.
    """
    prompts, labels, images, tools = [], [], [], []
    exhausted = False

    while len(prompts) < num_prompts:
        try:
            _, batch_prompts, batch_labels, batch_images, batch_tools = next(dataloader_iter)
            remaining = num_prompts - len(prompts)
            prompts.extend(batch_prompts[:remaining])
            labels.extend(batch_labels[:remaining])
            images.extend(batch_images[:remaining])
            tools.extend(batch_tools[:remaining])
        except StopIteration:
            exhausted = True
            break

    return prompts, labels, images, tools, exhausted


def _build_action_token_mask(
    num_tokens: int, action_ranges: List[Tuple[int, int]], off_policy_lens: Optional[List[int]] = None
) -> torch.BoolTensor:
    """Mask token positions generated by the policy in a full multi-turn trajectory.

    ``off_policy_lens`` (one per action range, optional): the count of LEADING
    action tokens that were generated under stale pre-broadcast weights during a
    partial rollout. Those positions are left False, so they get zero policy
    gradient AND drop out of the global token-mean denominator — slime's
    mask_offpolicy_in_partial_rollout, but folded into the action mask so the loss
    path is unchanged. It is also used for the streaming slow tail when a refit
    spans requests that outlive their originating generation call.
    """
    mask = torch.zeros(num_tokens, dtype=torch.bool)
    for i, (start, end) in enumerate(action_ranges):
        if start < 0 or end < start or end > num_tokens:
            raise ValueError(f"Invalid action range ({start}, {end}) for trajectory length {num_tokens}")
        off = min(off_policy_lens[i], end - start) if off_policy_lens else 0
        mask[start + off : end] = True
    return mask


def _sample_group_key(sample) -> int | str:
    """Stable prompt-group key when rollout samples carry grouping metadata."""
    sample_group_ids = getattr(sample, "group_ids", None)
    return sample_group_ids[0] if sample_group_ids else id(sample)


class SamplesGenerator:
    """Stream prompts through a persistent pool of rollout workers."""

    def __init__(
        self,
        strategy,
        prompts_dataloader,
        eval_dataloader,
        tokenizer,
        agent_runners,
    ):
        self.strategy = strategy
        self.args = strategy.args

        self.tokenizer = tokenizer
        # Runner actors driving rollouts through the vllm-router; prompts are round-robined
        # across them (self._rr) — no pool wrapper, just a list + an index.
        self.agent_runners = agent_runners
        self._rr = 0

        self.prompts_dataloader = prompts_dataloader
        self.eval_dataloader = eval_dataloader

    # Warm-resume (opt-in via --ckpt.warm_resume_rollouts). At every train step, save the rollout
    # groups that finished but were NOT trained yet — the surplus kept beyond the batch just shipped
    # (self._finished_samples) — so a resumed run trains them right away instead of idling ~one
    # generation while the pipeline refills. Each step writes a FRESH file (never overwritten), so a
    # resume restores exactly its own checkpoint's untrained groups. Only the file PATH is returned;
    # the groups themselves are large and must not travel the Ray queue. Best-effort: on any failure
    # the run just resumes normally (regenerating those groups).
    _BUFFER = "rollout_buffer.pt"

    def state_dict(self) -> Dict:
        """Save this step's untrained rollout groups to a fresh sidecar file, return its path."""
        finished = getattr(self, "_finished_samples", None)
        if not getattr(self.args.ckpt, "warm_resume_rollouts", False) or not finished:
            return {}
        try:
            d = os.path.join(os.path.dirname(self.args.ckpt.path.rstrip("/")), "rollout_warm")
            os.makedirs(d, exist_ok=True)
            self._warm_seq = getattr(self, "_warm_seq", 0) + 1
            path = os.path.join(d, f"{self._BUFFER}.{os.getpid()}.{self._warm_seq}")  # pid+seq: unique per step
            torch.save(list(finished), path)
            for stale in sorted(os.scandir(d), key=lambda e: e.stat().st_mtime)[:-3]:  # keep newest 3; bound disk
                os.remove(stale.path)
            return {"buffer_file": path}
        except Exception as e:  # bookkeeping must never break the checkpoint
            logger.warning(f"warm-resume: save skipped ({e})")
            return {}

    def load_state_dict(self, state_dict: Optional[Dict]) -> None:
        """Restore this checkpoint's untrained groups (if any) to seed the first post-resume batch."""
        path = (state_dict or {}).get("buffer_file")
        if not path or not os.path.exists(path):
            return  # no file / failed save -> normal resume
        try:
            self._resumed_samples = torch.load(path, map_location="cpu", weights_only=False)
            logger.info(f"warm-resume: restored {len(self._resumed_samples)} rollout groups")
        except Exception as e:
            logger.warning(f"warm-resume: load skipped ({e})")

    @torch.no_grad()
    def generate_eval_samples(self, **generate_kwargs) -> List[Experience]:
        """Generate evaluation samples for the entire eval dataloader."""
        if getattr(self, "_eval_dataloader_iter", None) is None:
            self._eval_dataloader_iter = iter(self.eval_dataloader)

        all_experiences: List[Experience] = []
        try:
            while True:
                experiences, _, exhausted = self._generate_batch(
                    dataloader_iter=self._eval_dataloader_iter,
                    num_prompts=self.args.rollout.batch_size,
                    dynamic_filtering=False,
                    **generate_kwargs,
                )
                all_experiences.extend(experiences)
                if exhausted:
                    break
        finally:
            self._eval_dataloader_iter = None

        return all_experiences

    @torch.no_grad()
    def generate_samples(self, **generate_kwargs) -> Tuple[List[Experience], Dict[str, float], int, bool]:
        """Stream one training-sized batch out of a continuously-refilled rollout pool.

        Keeps `vllm_generate_batch_size` prompt rollouts in flight at all times and
        returns as soon as `rollout.batch_size` prompt groups have *finished* — it
        does not wait for the slow tail of the dispatched batch. The unfinished
        rollouts (and any surplus finished groups) persist on the instance across
        calls, so vLLM never drains between training steps: generation of the next
        batch fully overlaps training of the current one.

        Multi-turn agents emit several step-samples per rollout, so we chunk by
        GROUP (= prompt): each returned batch holds `rollout.batch_size` prompts,
        each with its N rollouts × K_i step-samples. Safe with `partial_rollout` —
        a weight refit pauses/resumes the engines (see broadcast_to_vllm), so the
        in-flight rollouts survive it.
        """
        if getattr(self, "_dataloader_iter", None) is None:
            self._dataloader_iter = iter(self.prompts_dataloader)
            # Seed from a warm-resume buffer if load_state_dict restored one, so the first
            # post-resume batch ships without waiting for a full fresh generation. Consumed once.
            self._finished_samples: List[Experience] = list(getattr(self, "_resumed_samples", None) or [])
            self._resumed_samples = None
            self._inflight_rollouts: List = []

        groups_per_batch = self.args.rollout.batch_size
        inflight_capacity = getattr(self.args.rollout, "vllm_generate_batch_size", None) or groups_per_batch
        dynamic_filtering = self.args.algo.dynamic_filtering_enable

        def finished_group_count() -> int:
            return len({_sample_group_key(sample) for sample in self._finished_samples})

        prompts_dispatched = 0
        groups_accepted = 0
        drop_counts: Dict[str, int] = defaultdict(int)
        score_stats: Dict[str, float] = defaultdict(float)  # pre-DAPO-filter score stats
        progress = tqdm(
            total=groups_per_batch, initial=min(finished_group_count(), groups_per_batch), desc="Generate samples"
        )

        while finished_group_count() < groups_per_batch:
            # Refill so the runner pool keeps `inflight_capacity` rollouts in flight (engines stay saturated).
            free_slots = inflight_capacity - len(self._inflight_rollouts)
            if free_slots > 0 and self._dataloader_iter is not None:
                prompts, labels, images, tools, dataloader_exhausted = _collect_prompt_batch(
                    self._dataloader_iter, free_slots
                )
                prompts_dispatched += len(prompts)
                if prompts:
                    self._inflight_rollouts.extend(
                        self._dispatch_to_agent_runners(prompts, labels, images=images, tools=tools, **generate_kwargs)
                    )
                if dataloader_exhausted:
                    self._dataloader_iter = None
                    logger.info("Prompt dataloader is exhausted.")

            if not self._inflight_rollouts:
                break  # dataloader drained and pool empty — emit whatever finished

            # Take the first rollout to finish; the slow ones keep generating in vLLM.
            ready, self._inflight_rollouts = ray.wait(self._inflight_rollouts, num_returns=1)
            for finished_rollout in ready:
                # Dropped groups (filtered or all-unusable) come back empty; their
                # slot is refilled with a fresh prompt on the next iteration.
                group_samples = self._filter_group(
                    finished_rollout, dynamic_filtering, drop_counts, score_stats=score_stats, **generate_kwargs
                )
                if group_samples:
                    self._finished_samples.extend(group_samples)
                    groups_accepted += 1
                    progress.update(1)
        progress.close()

        # Observability: per-reason drop counts + (when filtering) the pass rate.
        rollout_metrics = {f"rollout/dropped/{reason}": float(n) for reason, n in drop_counts.items()}
        if drop_counts:
            rollout_metrics["rollout/dropped/total"] = float(sum(drop_counts.values()))
        if dynamic_filtering and prompts_dispatched:
            rollout_metrics["dynamic_filtering_pass_rate"] = groups_accepted / prompts_dispatched * 100
        # Pre-filter stats: the model's TRUE judge pass rate over ALL scored samples, BEFORE DAPO
        # drops uniform groups. (post-filter `reward`/`pivotrl_correct` only covers kept MIXED groups
        # ~0.5-0.65 by construction, so it hides the real pass rate + the all-pass saturation.)
        if score_stats.get("score_n", 0) > 0:
            rollout_metrics["rollout/pre_filter_mean_score"] = score_stats["score_sum"] / score_stats["score_n"]
        if score_stats.get("groups", 0) > 0:
            rollout_metrics["rollout/group_all_pass_rate"] = score_stats["all_pass"] / score_stats["groups"] * 100
            rollout_metrics["rollout/group_all_fail_rate"] = score_stats["all_fail"] / score_stats["groups"] * 100

        # Hand back exactly `groups_per_batch` finished groups; keep any surplus for the next call.
        selected_groups: set = set()
        batch_samples: List[Experience] = []
        leftover_samples: List[Experience] = []
        for sample in self._finished_samples:
            group_key = _sample_group_key(sample)
            if group_key in selected_groups or len(selected_groups) < groups_per_batch:
                selected_groups.add(group_key)
                batch_samples.append(sample)
            else:
                leftover_samples.append(sample)
        self._finished_samples = leftover_samples

        # Exhausted only once the dataloader is done AND nothing is buffered or in flight.
        exhausted = self._dataloader_iter is None and not self._finished_samples and not self._inflight_rollouts
        return batch_samples, rollout_metrics, prompts_dispatched, exhausted

    def _passes_dynamic_filter(self, group_samples) -> bool:
        """Whether a scored group's mean reward lands inside the dynamic-filtering range.

        A group with any unscored sample always passes — filtering only applies once
        every sample in the group has a score.
        """
        if not all(s.scores is not None for s in group_samples):
            return True
        scores = [s.scores[0].item() for s in group_samples]
        mean_score = sum(scores) / len(scores)
        min_score, max_score = self.args.algo.dynamic_filtering_range
        if min_score < mean_score < max_score:
            return True
        logger.info(
            f"Filtered out group: mean_score={mean_score:.2f}, range=({min_score:.2f}, {max_score:.2f}), "
            f"scores={[f'{s:.2f}' for s in scores]}"
        )
        return False

    def _filter_group(
        self,
        finished_rollout,
        dynamic_filtering: bool,
        drop_counts: Dict[str, int],
        score_stats: Dict[str, float] | None = None,
        **generate_kwargs,
    ) -> List[Experience]:
        """Filter one finished rollout (a prompt's N responses = one group) down to
        its kept Experiences, tallying every drop into ``drop_counts`` by reason.

        The single place the keep/drop policy lives, applying both filters:
        per-response (unusable trajectories, via ``_process_response_into_experience``)
        and the group-level DAPO dynamic-reward filter. Returns ``[]`` when the
        whole group is dropped. Both the streaming (``generate_samples``) and
        batch/eval (``_generate_batch``) paths call it, so the per-group filter loop
        is never reimplemented.
        """
        group_samples: List[Experience] = []
        for response in ray.get(finished_rollout):
            experience, drop_reason = self._process_response_into_experience(response, **generate_kwargs)
            if experience is not None:
                group_samples.append(experience)
            elif drop_reason is not None:
                drop_counts[drop_reason] += 1

        if dynamic_filtering and group_samples:
            # Pre-filter score stats (the model's TRUE judge pass rate over scored samples, BEFORE
            # DAPO drops uniform groups). Accumulate here, before any keep/drop decision, so the
            # logged mean reflects all-pass + all-fail + mixed (not just the kept mixed groups).
            if score_stats is not None:
                scored = [s.scores[0].item() for s in group_samples if s.scores is not None]
                if scored:
                    min_score, max_score = self.args.algo.dynamic_filtering_range
                    gmean = sum(scored) / len(scored)
                    score_stats["score_sum"] += sum(scored)
                    score_stats["score_n"] += len(scored)
                    score_stats["groups"] += 1.0
                    score_stats["all_pass"] += float(gmean >= max_score)
                    score_stats["all_fail"] += float(gmean <= min_score)
            # Require COMPLETE groups: a group that lost a response to a per-response drop
            # (vlm_truncation / no_action_tokens / logprob_misalign / ...) has < n_samples
            # usable samples, which would pull the accepted count off train_batch_size and make
            # it indivisible by the DP-rank count -> per-sample forward microbatches split unevenly
            # -> NCCL collective desync/hang. Drop+backfill the whole group so each accepted group
            # contributes exactly n_samples (batch stays a clean groups_per_batch * n_samples).
            n_samples = generate_kwargs.get("n_samples_per_prompt", self.args.rollout.n_samples_per_prompt)
            # Count ROLLOUTS, not step-samples: a multi-turn / context-compacting agent emits
            # several step-samples per rollout sharing one rollout_id (see experience.py), so
            # len(group_samples) over-counts and a group that actually lost a rollout could still
            # pass this completeness check. Count distinct rollout_ids (single-turn: 1 each, == old).
            n_rollouts = len({(s.rollout_ids[0] if getattr(s, "rollout_ids", None) else id(s)) for s in group_samples})
            if n_rollouts < n_samples:
                drop_counts["incomplete_group"] += len(group_samples)
                return []
            if not self._passes_dynamic_filter(group_samples):
                drop_counts["dynamic_filter"] += len(group_samples)
                return []
        return group_samples

    def _generate_batch(
        self, dataloader_iter, num_prompts: int, dynamic_filtering, **generate_kwargs
    ) -> Tuple[List[Experience], int, bool]:
        """Generate a batch of Experiences with optional reward filtering.

        Dispatches num_prompts across the runner actors (which generate through the
        vllm-router), collects all results, and returns. When dynamic_filtering is
        enabled, filtered prompts are replaced with new ones.
        """
        prompts_consumed = 0
        accepted_experiences: List[Experience] = []
        drop_counts: Dict[str, int] = defaultdict(int)

        prompts, labels, images, tools, exhausted = _collect_prompt_batch(dataloader_iter, num_prompts)
        if not prompts:
            return [], prompts_consumed, True

        target_num_prompts = len(prompts)
        pending_refs = self._dispatch_to_agent_runners(prompts, labels, images=images, tools=tools, **generate_kwargs)
        prompts_consumed += target_num_prompts

        pbar = tqdm(range(target_num_prompts), desc="Generate samples")

        while pending_refs:
            ready_refs, pending_refs = ray.wait(pending_refs, num_returns=1, timeout=10.0)
            for ref in ready_refs:
                # One ref == one prompt's full rollout (n_samples responses) = one
                # prompt group. Same keep/drop policy as the streaming path, via the
                # shared _filter_group (dynamic filtering is per-group, like DAPO).
                group_experiences = self._filter_group(ref, dynamic_filtering, drop_counts, **generate_kwargs)

                if group_experiences:
                    accepted_experiences.extend(group_experiences)
                    pbar.set_postfix({"prompts_consumed": prompts_consumed})
                    pbar.update()
                elif dynamic_filtering and not exhausted:
                    # Dispatch a replacement for the filtered prompt, but only
                    # while the dataloader still has prompts. Never ray.cancel
                    # the remaining in-flight rollouts: draining them keeps the
                    # already-generated valid experiences (and an accurate
                    # prompts_consumed count) instead of discarding work at the
                    # dataloader boundary.
                    new_prompts, new_labels, new_images, new_tools, exhausted = _collect_prompt_batch(
                        dataloader_iter, 1
                    )
                    prompts_consumed += len(new_prompts)
                    if new_prompts:
                        new_refs = self._dispatch_to_agent_runners(
                            new_prompts, new_labels, images=new_images, tools=new_tools, **generate_kwargs
                        )
                        pending_refs.extend(new_refs)

        if drop_counts:
            logger.info(f"Eval rollout drops: {dict(drop_counts)}")
        return accepted_experiences, prompts_consumed, exhausted

    def _dispatch_to_agent_runners(
        self, prompts: List[str], labels: List[str], *, images: List = None, tools: List = None, **generate_kwargs
    ) -> List:
        """Round-robin each prompt group onto the runner actors; each ``run_group`` returns a
        Ray ref of that group's Trajectories, consumed by the streaming loop / filtering /
        experience maker exactly as before."""
        sampling_params = SamplingParams(
            temperature=generate_kwargs.get("temperature", 1.0),
            top_p=generate_kwargs.get("top_p", 1.0),
            top_k=generate_kwargs.get("top_k", -1),
            max_tokens=generate_kwargs.get("max_new_tokens"),  # None = dynamic per-prompt
            min_tokens=generate_kwargs.get("min_new_tokens", 1),
            skip_special_tokens=generate_kwargs.get("skip_special_tokens", False),
            logprobs=1 if self.args.algo.advantage.is_correction_enable else None,
        )
        truncate_length = generate_kwargs.get("max_len", 2048)
        n_samples = generate_kwargs.get("n_samples_per_prompt", self.args.rollout.n_samples_per_prompt)
        if images is None:
            images = [None] * len(prompts)
        if tools is None:
            tools = [None] * len(prompts)

        refs = []
        for prompt, label, img, tool in zip(prompts, labels, images, tools):
            actor = self.agent_runners[self._rr % len(self.agent_runners)]
            self._rr += 1
            refs.append(
                actor.run_group.remote(prompt, label, img, sampling_params, truncate_length, n_samples, tools=tool)
            )
        return refs

    def _media_token_ids(self) -> set:
        """Image/video placeholder token ids from the tokenizer/processor (cached)."""
        cache = getattr(self, "_media_ids_cache", None)
        if cache is not None:
            return cache
        ids: set = set()
        tok = self.tokenizer
        for obj in (tok, getattr(tok, "tokenizer", None)):
            if obj is None:
                continue
            for attr in ("image_token_id", "video_token_id", "img_context_token_id", "video_context_token_id"):
                tid = getattr(obj, attr, None)
                if isinstance(tid, int):
                    ids.add(tid)
        self._media_ids_cache = ids
        return ids

    def _process_response_into_experience(
        self, response, **generate_kwargs
    ) -> Tuple[Optional[Experience], Optional[str]]:
        """Turn one Trajectory (from agent Runner) into an Experience.

        Returns ``(experience, None)`` when the rollout is usable, or
        ``(None, drop_reason)`` when it must be dropped — the reason is a short
        code (e.g. ``"vlm_truncation"``, ``"no_action_tokens"``) that
        ``_filter_group`` tallies into the ``rollout/dropped/<reason>`` metrics.
        """
        truncate_length = generate_kwargs.get("max_len", 2048)
        step_slice = slice(1, truncate_length)

        trajectory_tokens = response.observation_tokens.copy()
        if not trajectory_tokens:
            logger.warning("Skipping rollout response with empty observation_tokens.")
            return None, "empty_tokens"

        # VLM safety: the truncation below cuts the token sequence at
        # `truncate_length` but mm_train_inputs (pixel_values / image_grid_thw)
        # stays full-size. If truncation removes any image/video placeholder
        # token, the actor's vit-embed scatter misaligns with the pixel_values
        # rows — a SILENT train/rollout policy mismatch (actor.forward only
        # warns once). Drop such rollouts instead of training on corrupt data.
        if response.mm_train_inputs is not None and len(trajectory_tokens) > truncate_length:
            media_ids = self._media_token_ids()
            dropped_media = (
                sum(1 for t in trajectory_tokens[truncate_length:] if t in media_ids) if media_ids else None
            )
            if dropped_media is None:
                logger.warning(
                    "Skipping VLM rollout: trajectory exceeds max_len and image token ids could not be "
                    "resolved to verify truncation safety. Increase --max_len / --max_new_tokens."
                )
                return None, "vlm_media_unresolved"
            if dropped_media:
                logger.warning(
                    f"Skipping VLM rollout: truncation at max_len={truncate_length} would drop {dropped_media} "
                    "image/video placeholder token(s) while pixel_values stays full-size (vit-embed "
                    "misalignment). Increase --max_len or --max_new_tokens."
                )
                return None, "vlm_truncation"

        reward_val = _to_scalar(response.reward)
        score_val = _to_scalar(response.scores)

        sequences = torch.tensor(trajectory_tokens, dtype=torch.long)
        attention_mask = torch.ones(len(trajectory_tokens), dtype=torch.long)
        action_token_mask = _build_action_token_mask(
            len(trajectory_tokens), response.action_ranges, response.off_policy_action_lens
        )

        # Truncate token-axis tensors to the configured context window.
        sequences = sequences[:truncate_length].to("cpu")
        attention_mask = attention_mask[:truncate_length].to("cpu")

        # Step-axis tensors align with next-token logprobs: token position k is
        # predicted from position k-1, so we drop token position 0. Multi-turn
        # environment/tool feedback stays on this dense axis with mask=False.
        action_mask = action_token_mask[step_slice].to("cpu")
        raw_rollout_log_probs = response.rollout_log_probs
        if raw_rollout_log_probs is not None:
            if len(raw_rollout_log_probs) != len(trajectory_tokens):
                logger.warning(
                    "rollout_log_probs must align with observation_tokens before the next-token shift: "
                    f"got {len(raw_rollout_log_probs)} logprobs for {len(trajectory_tokens)} tokens; "
                    "skipping rollout response."
                )
                return None, "logprob_misalign"
            rollout_log_probs = torch.tensor(raw_rollout_log_probs[step_slice]).to("cpu")
        else:
            rollout_log_probs = None

        # Number of generated action tokens (multi-turn observation gaps don't count).
        response_length = int(action_mask.sum().item())
        if response_length == 0:
            logger.warning("Skipping rollout response with no trainable action tokens.")
            return None, "no_action_tokens"
        total_length = attention_mask.float().sum()
        is_clipped = total_length >= truncate_length

        info = {"response_clip_ratio": torch.tensor([is_clipped])}
        if response.mm_train_inputs is not None and (media_ids := self._media_token_ids()):
            # Embedded image tokens that actually enter training. An image silently dropped
            # or rendered at reduced resolution shrinks this count while reward/vllm_kl stay
            # plausible — the one visible trace of an image-blind rollout.
            info["image_tokens"] = torch.tensor(
                [sum(1 for t in trajectory_tokens[:truncate_length] if t in media_ids)]
            )
        if reward_val is not None:
            info["reward"] = torch.tensor([reward_val])
        if score_val is not None:
            info["score"] = torch.tensor([score_val])

        # Convert extra logs to tensors for downstream consumers. Skip
        # non-numeric values (e.g. string task ids) —
        # `torch.tensor([str])` raises "too many dimensions 'str'".
        for key, value in (response.extra_logs or {}).items():
            value = _to_scalar(value)
            if isinstance(value, (int, float, bool)):
                info[key] = torch.tensor([value])

        # R3: per-token rollout routing aligned with `sequences` (one [L, K] expert-id
        # row per token), seq last after the permute below.
        routed_experts = None
        if response.routed_experts is not None:
            row_shape = next((r.shape for r in response.routed_experts if r is not None), None)
            if row_shape is not None:
                # Positions with no captured rollout routing (a multimodal prompt the engine
                # didn't route, feedback, the trailing token) get a -1 sentinel: the training
                # forward keeps its own natural routing there (RouterReplay). Zeros would
                # instead force those tokens to expert 0 and corrupt the forward.
                sentinel = np.full(row_shape, -1, dtype=np.int16)
                rows = [r if r is not None else sentinel for r in response.routed_experts]
                # int16, not the engine's uint8: the training side pads the CP tail with the -1
                # sentinel, which on uint8 wraps to 255 and (for models with <256 experts, e.g.
                # omni3's 128) indexes out of bounds -> device-side assert in the router gather.
                arr = np.stack(rows).astype(np.int16)[:truncate_length]  # (T, L, K), aligned with sequences
                routed_experts = torch.from_numpy(arr).permute(1, 2, 0).contiguous().unsqueeze(0)  # (1, L, K, T)

        experience = Experience(
            sequences=sequences.unsqueeze(0),
            attention_mask=attention_mask.unsqueeze(0),
            action_mask=action_mask.unsqueeze(0),
            rollout_log_probs=rollout_log_probs.unsqueeze(0) if rollout_log_probs is not None else None,
            routed_experts=routed_experts,
            prompts=[response.prompt],
            labels=[response.label],
            images=[response.images],
            mm_train_inputs=[response.mm_train_inputs],
            group_ids=[response.group_id] if response.group_id is not None else [],
            rollout_ids=[response.rollout_id] if response.rollout_id is not None else [],
            rewards=torch.tensor([reward_val]) if reward_val is not None else None,
            scores=torch.tensor([score_val]) if score_val is not None else None,
            response_length=torch.tensor([response_length]),
            truncated=torch.tensor([response.truncated]),
            total_length=torch.tensor([total_length]),
            info=info,
        )
        return experience, None

# ---------------------------------------------------------------------------
# Fakes: the transport exposes generate(token_ids, sp, multi_modal_data) over the
# router's /v1/completions (same call the step runner uses). Client-side templating
# (apply_chat_template + _tokenize_observation) is monkeypatched so tests drive the
# exact prompt token ids per turn.
# ---------------------------------------------------------------------------
class _FakeTransport:
    def __init__(self, actions):  # actions: list of (action_ids, logprobs, finish, routed)
        self.actions = list(actions)
        self.calls = []

    async def generate(self, prompt_token_ids, sampling_params, multi_modal_data=None, session_id=None):
        self.calls.append((list(prompt_token_ids), multi_modal_data, sampling_params.max_tokens))
        aids, lps, finish, routed = self.actions.pop(0)
        logprobs = [{t: SimpleNamespace(logprob=lp)} for t, lp in zip(aids, lps)]
        gen = SimpleNamespace(
            token_ids=list(aids),
            text="ACT",
            finish_reason=finish,
            logprobs=logprobs,
            routed_experts=np.asarray(routed) if routed is not None else None,
        )
        return SimpleNamespace(outputs=[gen], prompt_routed_experts=None), 0


def _proc():
    # apply_chat_template output is fed to the monkeypatched _tokenize_observation, so its value is
    # irrelevant; image_token == "<image>" -> should_expand is False (no structured-content split).
    return SimpleNamespace(apply_chat_template=lambda chat, **k: "TEXT", image_token="<image>")


def _sampling(max_tokens=8):
    return SimpleNamespace(max_tokens=max_tokens, temperature=1.0, top_p=1.0, logprobs=1, min_tokens=0)


def _state(actions, *, max_length=1000):
    st = ChatServerState(_FakeTransport(actions), _proc(), "policy", max_length, _sampling())
    return st, st.transport


def _act(action_ids, logprobs, *, finish="stop", routed=None):
    return (action_ids, logprobs, finish, routed)


def _patch_prompts(monkeypatch, prompts, *, mm=None, pil=None):
    """Drive the per-turn prompt token ids (what apply_chat_template + processor would produce)."""
    q = list(prompts)
    monkeypatch.setattr(cs, "_tokenize_observation", lambda proc, text, imgs: (q.pop(0), mm, pil or []))


_MSG = {"messages": [{"role": "user", "content": "P"}]}


# ---------------------------------------------------------------------------
# Multi-turn fakes: a char-level template + tokenizer that deliberately does NOT round-trip
# generated bytes — it TRIMS a trailing </think> when rendering an assistant turn (like qwen3.6)
# and wraps assistants in tags. A naive full re-render of turn N would therefore drift from the
# exact generated tokens; the TITO delta path must not. The engine decodes action text from the
# ids (like the token-only router) and can tag its prefill/gen routing per turn.
# ---------------------------------------------------------------------------
class _DriftyProc:
    image_token = "<image>"  # literal placeholder -> no structured-content split

    def apply_chat_template(self, chat, tokenize=False, add_generation_prompt=False, **kwargs):
        out = ""
        for m in chat:
            content = m["content"]
            if m["role"] == "assistant" and content.endswith("</think>"):
                content = content[: -len("</think>")]  # non-round-trip trim
            out += f"<{m['role']}>{content}</{m['role']}>"
        return out + ("<gen>" if add_generation_prompt else "")

    def __call__(self, text=None, add_special_tokens=False, return_tensors=None):
        return {"input_ids": torch.tensor([[ord(c) for c in text]])}

    def decode(self, ids, skip_special_tokens=False):
        return "".join(chr(i) for i in ids)


class _DriftyEngine:
    def __init__(self, actions):  # actions: (action_ids, logprobs, finish, tag)
        self.actions = list(actions)
        self.prompts = []

    async def generate(self, prompt_token_ids, sp, multi_modal_data=None, session_id=None):
        self.prompts.append(list(prompt_token_ids))
        aids, lps, finish, tag = self.actions.pop(0)
        logprobs = [{t: SimpleNamespace(logprob=lp)} for t, lp in zip(aids, lps)]
        # tag!=None: routing per turn. gen omits the LAST action token (a trailing sampled token has
        # no routing at gen time); the prefill (prompt routing) covers the whole prefix, so the next
        # turn backfills that boundary position first-writer-wins.
        gen_re = np.asarray([[[tag]]] * (len(aids) - 1), dtype=np.int16) if tag is not None else None
        prompt_re = np.asarray([[[tag]]] * len(prompt_token_ids), dtype=np.int16) if tag is not None else None
        gen = SimpleNamespace(
            token_ids=list(aids), text="", finish_reason=finish, logprobs=logprobs, routed_experts=gen_re
        )
        return SimpleNamespace(outputs=[gen], prompt_routed_experts=prompt_re), 0


def _dact(action_ids, logprobs, *, finish="stop", tag=None):
    return (action_ids, logprobs, finish, tag)


def _ids(s):
    return [ord(c) for c in s]


def _drive(state, session, messages):
    return asyncio.run(_run_turn(state, session, {"messages": messages}))



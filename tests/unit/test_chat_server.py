def test_run_turn_records_exact_tokens(monkeypatch):
    _patch_prompts(monkeypatch, [[1, 2, 3]])
    state, tp = _state([_act([90, 91], [-0.3, -0.4])])
    state.open("sid", "P", "lab", None)
    session = state.sessions["sid"]

    action, finish = asyncio.run(_run_turn(state, session, _MSG))

    assert (action, finish) == ("ACT", "stop")
    assert tp.calls[0][0] == [1, 2, 3]  # generate fed the tokenized prompt
    traj = session.trajectories[0]
    assert traj.observation_tokens == [1, 2, 3, 90, 91]
    assert traj.action_ranges == [(3, 5)]
    assert traj.rollout_log_probs == [0.0, 0.0, 0.0, -0.3, -0.4]
    assert traj.prompt == "P" and traj.label == "lab"


def test_multiturn_trajectory_is_token_exact_and_unfragmented():
    # Drive 3 real turns through the drifty (non-round-tripping) template. The accumulated trajectory
    # must be EXACTLY turn1_prompt + action1 + delta2 + action2 + delta3 + action3, each action's
    # tokens byte-identical to what the engine generated — one unfragmented, drift-free trajectory.
    a1, a2, a3 = "A1</think>", "A2</think>", "A3"
    engine = _DriftyEngine(
        [_dact(_ids(a1), [-0.1] * len(a1)), _dact(_ids(a2), [-0.2] * len(a2)), _dact(_ids(a3), [-0.3] * len(a3))]
    )
    state = ChatServerState(engine, _DriftyProc(), "policy", 10000, _sampling())
    state.open("sid", "P", "lab", None)
    session = state.sessions["sid"]

    msgs = [{"role": "user", "content": "Q0"}]
    act1, _ = _drive(state, session, msgs)
    # Stateless chat: append the model's reply + a tool observation, resend the whole history.
    msgs = msgs + [{"role": "assistant", "content": act1}, {"role": "user", "content": "OBS1"}]
    act2, _ = _drive(state, session, msgs)
    msgs = msgs + [{"role": "assistant", "content": act2}, {"role": "user", "content": "OBS2"}]
    act3, _ = _drive(state, session, msgs)
    assert (act1, act2, act3) == (a1, a2, a3)  # decoded actions keep </think> (no template trim)

    proc = _DriftyProc()

    def render(chat, gen):
        return proc.apply_chat_template(chat, add_generation_prompt=gen)

    turn1_prompt = _ids(render([{"role": "user", "content": "Q0"}], True))
    delta2 = _ids(render([{"role": "user", "content": "OBS1"}], True))  # suffix after the shared prefix
    delta3 = _ids(render([{"role": "user", "content": "OBS2"}], True))
    expected = turn1_prompt + _ids(a1) + delta2 + _ids(a2) + delta3 + _ids(a3)

    traj = session.trajectories[0]
    assert traj.observation_tokens == expected  # token-exact, single growing trajectory
    assert engine.prompts[1] == turn1_prompt + _ids(a1) + delta2  # turn 2 generated over the exact tokens
    assert [traj.observation_tokens[s:e] for s, e in traj.action_ranges] == [_ids(a1), _ids(a2), _ids(a3)]
    assert traj.rollout_log_probs == (
        [0.0] * len(turn1_prompt)
        + [-0.1] * len(a1)
        + [0.0] * len(delta2)
        + [-0.2] * len(a2)
        + [0.0] * len(delta3)
        + [-0.3] * len(a3)
    )
    # Proof the fix matters: a naive full re-render of the history does NOT reproduce turn-1's exact
    # tokens (the template trims </think> + wraps the assistant), so the old prefix-merge would split.
    turn1_exact = turn1_prompt + _ids(a1)
    naive = _ids(render(msgs, True))
    assert naive[: len(turn1_exact)] != turn1_exact


def test_multiturn_routing_backfills_turn_boundary():
    # R3 routing is laid down per turn (absorb_routing, first-writer-wins). Assert it stays aligned
    # 1:1 with tokens and that a prior action's trailing token — unrouted at gen time — is backfilled
    # by the next turn's prefill, exactly like the old merge did explicitly.
    engine = _DriftyEngine([_dact(_ids("X1"), [-0.1, -0.1], tag=11), _dact(_ids("Y2"), [-0.2, -0.2], tag=22)])
    state = ChatServerState(engine, _DriftyProc(), "policy", 10000, _sampling())
    state.open("sid", "P", "l", None)
    session = state.sessions["sid"]
    msgs = [{"role": "user", "content": "Q0"}]
    act1, _ = _drive(state, session, msgs)
    msgs = msgs + [{"role": "assistant", "content": act1}, {"role": "user", "content": "OBS1"}]
    _drive(state, session, msgs)

    proc = _DriftyProc()
    p1 = len(_ids(proc.apply_chat_template([{"role": "user", "content": "Q0"}], add_generation_prompt=True)))
    traj = session.trajectories[0]
    re = traj.routed_experts

    def val(i):
        return None if re[i] is None else int(re[i][0][0])

    assert len(re) == len(traj.observation_tokens)  # aligned 1:1 with tokens
    assert all(val(i) == 11 for i in range(p1))  # shared prefix keeps turn-1 routing (first-writer-wins)
    assert val(p1) == 11 and val(p1 + 1) == 22  # action1 trailing token backfilled by turn-2 prefill
    assert re[-1] is None and all(r is not None for r in re[:-1])  # only the final trailing token unrouted


def test_compaction_starts_a_fresh_segment():
    # An agent that rewrites/shortens history (compaction) no longer prefix-extends the running
    # segment, so a fresh token-exact segment starts — NOT a token-drift split. Both segments are
    # returned and carry the terminal reward (one rollout_id -> deduped to one reward downstream).
    a1, a2, a3 = "A1", "A2", "A3"
    engine = _DriftyEngine(
        [_dact(_ids(a1), [-0.1] * len(a1)), _dact(_ids(a2), [-0.2] * len(a2)), _dact(_ids(a3), [-0.3] * len(a3))]
    )
    state = ChatServerState(engine, _DriftyProc(), "policy", 10000, _sampling())
    state.open("sid", "P", "lab", None)
    session = state.sessions["sid"]

    msgs = [{"role": "user", "content": "Q0"}]
    r1, _ = _drive(state, session, msgs)
    msgs = msgs + [{"role": "assistant", "content": r1}, {"role": "user", "content": "OBS1"}]
    _drive(state, session, msgs)  # normal continuation -> same segment
    assert len(session.trajectories) == 1 and len(session.trajectories[0].action_ranges) == 2

    # Compaction: the agent replaces the whole history with a summary — the prefix no longer matches.
    compacted = [{"role": "user", "content": "SUMMARY"}]
    r3, _ = _drive(state, session, compacted)
    assert len(session.trajectories) == 2  # a genuine rewrite -> new segment, not a drift split
    proc = _DriftyProc()
    expected2 = _ids(proc.apply_chat_template(compacted, add_generation_prompt=True)) + _ids(r3)
    assert session.trajectories[1].observation_tokens == expected2  # segment 2 token-exact from compacted
    assert [len(t.action_ranges) for t in session.trajectories] == [2, 1]

    out = stitch_session(state, "sid", Result(reward=1.0, score=0.5))
    assert len(out) == 2 and all(t.reward == 1.0 and t.scores == 0.5 for t in out)



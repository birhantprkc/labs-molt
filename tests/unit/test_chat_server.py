def test_stitch_returns_one_trajectory_stamped_with_reward():
    # A multi-turn episode is ONE trajectory (an action range per turn), stamped with the terminal
    # reward/score — not one sample per turn.
    engine = _DriftyEngine([_dact(_ids("A1"), [-0.1, -0.1]), _dact(_ids("A2"), [-0.2, -0.2])])
    state = ChatServerState(engine, _DriftyProc(), "policy", 10000, _sampling())
    state.open("sid", "P", "l", None)
    session = state.sessions["sid"]
    msgs = [{"role": "user", "content": "Q0"}]
    act1, _ = _drive(state, session, msgs)
    _drive(state, session, msgs + [{"role": "assistant", "content": act1}, {"role": "user", "content": "OBS1"}])
    out = stitch_session(state, "sid", Result(reward=1.0, score=0.5))
    assert len(out) == 1
    assert len(out[0].action_ranges) == 2  # both turns on the one trajectory
    assert out[0].reward == 1.0 and out[0].scores == 0.5


